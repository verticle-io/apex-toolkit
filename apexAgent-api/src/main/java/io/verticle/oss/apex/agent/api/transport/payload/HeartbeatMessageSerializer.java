package io.verticle.oss.apex.agent.api.transport.payload;


import com.fasterxml.jackson.core.JsonGenerator;
import com.fasterxml.jackson.databind.JsonSerializer;
import com.fasterxml.jackson.databind.SerializerProvider;

import java.io.IOException;

/**
 *
 * @author Jens Saade
 */
public final class HeartbeatMessageSerializer extends JsonSerializer<HeartbeatMessage> {
    @Override
    public void serialize(HeartbeatMessage t, JsonGenerator jsonGenerator, SerializerProvider serializerProvider) throws IOException {
        jsonGenerator.writeStartObject();


        jsonGenerator.writeObjectField("meta", t.meta);

        jsonGenerator.writeEndObject();

    }
}
